//fonction pour ajouter un actif le jour
function addListner() {
  const days = document.querySelectorAll(".day");
  days.forEach((day) => {
    day.addEventListener("click", (e) => {
      getActiveDay(e.target.innerHTML);
      updateEvents(Number(e.target.innerHTML));
      activeDay = Number(e.target.innerHTML);

      //supprimer l'actif
      days.forEach((day) => {
        day.classList.remove("active");
      });

      //si vous cliquez sur la date précédente ou la date suivante, passez à ce mois
      if (e.target.classList.contains("prev-date")) {
        prevMonth();

        //ajouter un actif au clic jour après mois est un changement
        setTimeout(() => {
          //ajouter un actif là où il n'y a pas de date précédente ou de date suivante
          const days = document.querySelectorAll(".day");
          days.forEach((day) => {
            if (
              !day.classList.contains("prev-date") &&
              day.innerHTML === e.target.innerHTML
            ) {
              day.classList.add("active");
            }
          });
        }, 100);
      } else if (e.target.classList.contains("next-date")) {
        nextMonth();
        // ajouter l'actif au jour cliqué après le mois est modifié
        setTimeout(() => {
          const days = document.querySelectorAll(".day");
          days.forEach((day) => {
            if (
              !day.classList.contains("next-date") &&
              day.innerHTML === e.target.innerHTML
            ) {
              day.classList.add("active");
            }
          });
        }, 100);
      } else {
        e.target.classList.add("active");
      }
    });
  });
}

